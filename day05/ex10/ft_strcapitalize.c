char	*ft_strcapitalize(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			if (('a' <= str[i - 1] && str[i - 1] <= 'z') ||
					('A' <= str[i - 1] && str[i - 1] <= 'Z') ||
					('0' <= str[i - 1] && str[i - 1] <= '9'))
				str[i] += 32;
		}
		else if ('a' <= str[i] && str[i] <= 'z')
		{
			if (!(('a' <= str[i - 1] && str[i - 1] <= 'z') ||
					('A' <= str[i - 1] && str[i - 1] <= 'Z') ||
					('0' <= str[i - 1] && str[i - 1] <= '9')))
				str[i] -= 32;
		}
		i++;
	}
	return (str);
}
