int		ft_str_is_uppercase(char *str)
{
	int		i;
	int		upper;

	i = 0;
	upper = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || 'Z' < str[i])
			upper = 0;
		i++;
	}
	return (upper);
}
